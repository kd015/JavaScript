let day = 6
switch(day){
    case 0:
        console.log("Sunday")
        break;
    case 1:
        console.log("Mon")
        break;
    case 2:
        console.log("Tes")
        break;
    case 3:
        console.log("Wed")
        break;
    case 4:
        console.log("Thur")
        break;
    case 5:
        console.log("Fri")
        break;
    case 6:
        console.log("Sat")
        break;
    default:
        console.log("Invalid!!")
}       